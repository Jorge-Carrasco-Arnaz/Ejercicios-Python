# Pedir dos numero enteros y muestre por pantalla la división (n1) entre (n2), de cociente y resto . En solo 3 lineas

n1 = int(input("Introduce el primer número entero : "))
n2 = int(input("Introduce el segundo número entero : "))
print(n1, "entre " ,n2, "da un cociente de ", (n1 // n2),"y un resto de", (n1 % n2))
