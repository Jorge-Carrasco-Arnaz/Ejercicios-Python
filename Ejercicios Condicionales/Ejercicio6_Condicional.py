# Descripción: Escribir un programa que pida al usuario un número entero y muestre por pantalla si es par o impar.
# Entrada: Un número entero
# Salida: Un mensaje indicando si el número es par o impar

n1 = int(input("Introduzca un número entero: ")) # Pide al usuario que introduzca un número entero

if n1 % 2 == 0: # Comprueba si el número es par
    print("El número es par") # Imprime por pantalla el mensaje de que el número es par
else: # Ya que no es par, por lo tanto es impar
    print("El número es impar") # Imprime por pantalla el mensaje de que el número es impar

