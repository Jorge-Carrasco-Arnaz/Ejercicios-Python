# Descripción: Escribir un programa que muestre por pantalla la tabla de multiplicar del 1 al 10.
# Entrada: No tiene
# Salida: La tabla de multiplicar del 1 al 10

for i in range(1, 11): # Recorre todos los números del 1 al 10
    for j in range(1, 11): # Recorre todos los números del 1 al 10
        print(i, "x", j, "=", i * j) # Muestra la tabla de multiplicar del 1 al 10

